       <p>
          <h4>Comment activer votre compte ?</h4>
            Comment activer votre compte ?
            C'est très simple, il suffit de cliquer sur la barre de menu dans le coin supérieur droit  <b><i>Authentification </i></b>. 
            Après avoir cliqué, vous verrez une fenêtre popup qui s'ouvre sur le menu Se connecter, veuillez cliquer sur le menu <b><i>S'authentifier </i></b>. .
            Vous pouvez également cliquer sur le lien <b><i>S'authentifier</i></b> dans la phrase du formulaire de connexion, ce qui vous ramènera au formulaire d'authentification. 
            Vous pourrez alors soumettre votre demande d'activation de compte en saisissant votre <b><i>Numéro IFU</i></b>, votre <b><i>Mot de passe</i></b> et <b><i>Confirmer mot de passe</i></b> dans le formulaire.
            Après avoir rempli toutes les informations, vous pouvez maintenant cliquer sur le bouton <b><i>Enregistrer</i></b>.
      </p>
        
      <p>
          <h4>Comment se connecter ? </h4>
           Rien de très compliqué, il vous suffit de cliquer sur la barre de menu dans le coin supérieur droit <b><i>Authentification </i></b>. Après avoir cliqué, vous verrez 
           une fenêtre popup s'ouvrir dans le menu Connexion, il vous suffit d'entrer <b><i>Numero IFU</i></b>, votre <b><i>Mot de passe</i></b> dans le formulaire. Une fois que vous avez rempli toutes les informations,
           vous pouvez maintenant cliquer sur le bouton <b><i>Envoyer</i></b>. Si vous ne souhaitez pas saisir votre numéro IFU et votre mot de passe à chaque fois, vous devez cocher la case <b><i>Se souvenir de moi</i></b> lors de votre première connexion.
      </p>

      <p>
          <h4> Que faire si vous avez oublié votre mot de passe ?</h4>
          Pour résoudre ce problème, vous devez cliquer sur le lien Mot de passe oublié, qui vous enverra sur une page où vous verrez le formulaire <b><i>Lien de réintialisation</i></b>. Vous devez alors saisir votre adresse électronique
          pour réinitialiser votre mot de passe. Ensuite, il vous suffit de cliquer sur le bouton <b><i>Envoyer le lien</i></b>. Vous devez ensuite vérifier votre compte Gmail et vous verrez l'e-mail qui vous a été envoyé.
          Dans ce mail, vous devez cliquer sur le bouton que vous verrez qui vous redirigera vers une page de notre plateforme. Sur cette page, vous pourrez changer votre mot de passe afin d'avoir un nouveau mot de passe pour vous connecter.
      </p>

      <p>
          <h4> Comment puis-je voir les services fournis par l'entreprise ROMAS ?</h4> 

          Vous devez cliquer sur la barre de menu en haut à droite le menu <b><i>Services</i></b> qui vous montrera  sur la page les services offerts par l'entreprise ROMAS.
          
      </p>

      <p>
          <h4> Comment consulter les produits de l'entreprise ROMAS ?</h4> 
